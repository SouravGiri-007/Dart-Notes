  void main() {
  var choice = 2;

  switch (choice) {
    case 1:
      print('You chose option 1');
      break;
    case 2:
      print('You chose option 2');
      break;
    case 3:
      print('You chose option 3');
      break;
    default:
      print('Invalid choice');
  }
}


